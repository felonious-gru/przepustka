	  Az=Mpu6050.Accel_raw_Z/ 16384.0;
	  Gx=Mpu6050.Gyro_raw_X/ 131.0;
	  Gy=Mpu6050.Gyro_raw_Y/ 131.0;
	  Gz=Mpu6050.Gyro_raw_Z/ 131.0;
